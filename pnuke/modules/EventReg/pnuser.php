function eventreg_user_registerevent($args)
{
    list($eventid,
        $objectid) = pnVarCleanFromInput('eventid',
        'objectid'); 
    // Create output object - this object will store all of our output so that
    // we can return it easily when required
    extract($args);
    $output = new pnHTML();

    if (!empty($objectid)) {
        $eventid = $objectid;
    } 
    // Security check - important to do this as early as possible to avoid
    // potential security holes or just too much wasted processing
    if (!pnSecAuthAction(0, 'EventReg::', '::', ACCESS_COMMENT)) {
    	$output->SetInputMode(_PNH_PARSEINPUT); 
        $output->Text(_EVENTREGNOAUTH);
	//echo "userid=[".pnUserGetVar('uid')."]<br>";
	if(!pnUserGetVar('uid')){
		//not logged in, tell need to be registered site user
		$output->Text(_EVENTREGMUSTBEUSER);
		}
        return $output->GetOutput();
    } 
    if (!pnModAPILoad('EventReg', 'user')) {
        $output->Text(_LOADFAILED);
        return $output->GetOutput();
    } 
    // Add menu to output - it helps if all of the module pages have a standard
    // menu at their head to aid in navigation
    $output->SetInputMode(_PNH_VERBATIMINPUT);
    $output->Text(eventreg_usermenu());
    $output->SetInputMode(_PNH_PARSEINPUT); 
    // Title - putting a title ad the head of each page reminds the user what
    // they are doing
    $output->Title(_REGISTEREVENT);

    if (is_null($eventid) || empty($eventid)) {
        $output->SetOutputMode(_PNH_KEEPOUTPUT);
        $output->Text(_PLEASE . _SELECTEVENT);
        $output->FormStart(pnModURL('EventReg', 'user', 'registerevent'));
        $sel_data = pnModAPIFunc('EventReg', 'user', 'getallevents');
        foreach ($sel_data as $data) {
            $sel2_data[] = array('id' => $data['eventid'], 'name' => $data['name']);
        } 
        $output->FormSelectMultiple('eventid', $sel2_data);
        $output->SetInputMode(_PNH_PARSEINPUT);
        $output->LineBreak(1);
        $output->FormSubmit(_SELECTEVENTFROMLIST);
        $output->FormEnd();
        return $output->GetOutput();
    } 

	//Start routine to check if registration window is still open
	// added by Brian M 12-23-04
	If(!pnModAPIFunc('EventReg', 'user', 'checkregopen', array('event_id' => $eventid))) {
		$output->Text(_EVENTREG_REGWINDOWCLOSED2);
        return $output->GetOutput();
        
	}
	//End registration window check

    // Start of routine to check if this event has reached the max_regs threshold.
    // Added this check for EventReg .511
    $event = pnModAPIFunc('EventReg', 'user', 'getevent', array('eventid' => $eventid));
    $registrations = pnModAPIFunc('EventReg','user','countregistrationsforevent',
		    array('eventid' => $eventid));


    if ($registrations >= $event['max_regs']) {
        $output->Text(_MAXREGSREACHED);
        return $output->GetOutput();
    } 

    // End of max_regs check routine added for EventReg .511
	
	//Check that this user hasn't already registered for the same event (only allowed to register once)
	if(pnModAPIFunc('EventReg', 'user', 'alreadyregistered', array('event_id' => $eventid))) {
		$output->Text(_ALREADYREGISTERED);
        return $output->GetOutput();
	}
	//End already registered check
    $output->FormStart(pnModURL('EventReg', 'user', 'createregistration')); 
    // Add an authorisation ID - this adds a hidden field in the form that
    // contains an authorisation ID.  The authorisation ID is very important in
    // preventing certain attacks on the website
    $output->FormHidden('authid', pnSecGenAuthKey());
    $output->FormHidden('eventid', $eventid); 
